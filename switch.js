let grade = 'B';

switch (grade) {
  case 'A':
    console.log("Kerja bagus!");
    break;
  case 'B':
    console.log("Kerja yang baik!");
    break;
  case 'C':
    console.log("Kerja cukup!");
    break;
  case 'D':
    console.log("Kerja kurang!");
    break;
  case 'F':
    console.log("Kerja sangat buruk!");
    break;
  default:
    console.log("Nilai tidak valid");
}